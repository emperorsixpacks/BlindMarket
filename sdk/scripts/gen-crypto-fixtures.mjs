#!/usr/bin/env node
/**
 * Generates cross-compat crypto fixtures using Node's built-in crypto, matching
 * backend/src/services/crypto.ts byte-for-byte. The SDK's test/crypto.compat.test.ts
 * loads these and verifies WebCrypto-based decryption produces the same plaintext.
 *
 * Run:  node scripts/gen-crypto-fixtures.mjs
 * Output: test/fixtures/crypto.json (committed to the repo; regenerate only on
 *         intentional wire-format changes).
 */
import { createCipheriv, createECDH, hkdfSync, randomBytes } from 'node:crypto';
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const IV_LENGTH = 12;
const TAG_LENGTH = 16;
const KEY_LENGTH = 32;
const ECIES_HKDF_INFO = 'BlindMarket-ECIES-v1';

function aesEncrypt(plaintext, key) {
  const iv = randomBytes(IV_LENGTH);
  const cipher = createCipheriv('aes-256-gcm', key, iv, { authTagLength: TAG_LENGTH });
  const encrypted = Buffer.concat([cipher.update(plaintext), cipher.final()]);
  const tag = cipher.getAuthTag();
  // [IV][tag][ciphertext] — matches backend/src/services/crypto.ts
  return Buffer.concat([iv, tag, encrypted]);
}

function eciesEncrypt(plaintext, recipientPubKeyBuf) {
  const ephemeral = createECDH('secp256k1');
  ephemeral.generateKeys();
  const shared = ephemeral.computeSecret(recipientPubKeyBuf);
  const aesKey = Buffer.from(hkdfSync('sha256', shared, '', ECIES_HKDF_INFO, KEY_LENGTH));
  const aesBlob = aesEncrypt(plaintext, aesKey);
  return Buffer.concat([ephemeral.getPublicKey(), aesBlob]);
}

const recipient = createECDH('secp256k1');
recipient.generateKeys();
const privateKey = recipient.getPrivateKey('hex');
const publicKey = recipient.getPublicKey('hex', 'uncompressed');

const plaintext = 'hello cross-compat';
const ptBuf = Buffer.from(plaintext, 'utf-8');

// AES fixture
const aesKey = randomBytes(KEY_LENGTH);
const aesBlob = aesEncrypt(ptBuf, aesKey);

// ECIES fixture (recipient's pubkey is encoded as Buffer for computeSecret)
const eciesBlob = eciesEncrypt(ptBuf, Buffer.from(publicKey, 'hex'));

const fixture = {
  _note:
    'Generated by scripts/gen-crypto-fixtures.mjs using Node built-in crypto, matching backend/src/services/crypto.ts. If the SDK decrypts these back to the expected plaintext, wire-format compatibility is proven. Regenerate only on intentional format changes.',
  hkdf_info: ECIES_HKDF_INFO,
  keypair: { privateKey, publicKey },
  plaintext,
  aes: {
    key: aesKey.toString('hex'),
    blob: aesBlob.toString('hex'),
  },
  ecies: {
    blob: eciesBlob.toString('hex'),
  },
};

const __dirname = dirname(fileURLToPath(import.meta.url));
const out = resolve(__dirname, '..', 'test', 'fixtures', 'crypto.json');
writeFileSync(out, `${JSON.stringify(fixture, null, 2)}\n`);
console.log(`wrote ${out}`);
